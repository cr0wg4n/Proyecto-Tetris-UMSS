from random import randrange as rand

tetris_shapes = [
    [[1, 1, 1],
     [0, 1, 0]],

    [[0, 2, 2],
     [2, 2, 0]],

    [[3, 3, 0],
     [0, 3, 3]],

    [[4, 0, 0],
     [4, 4, 4]],

    [[0, 0, 5],
     [5, 5, 5]],

    [[6, 6, 6, 6]],

    [[7, 7],
     [7, 7]]
]

class ListShapes:
    def __init__(self, name):
        self.name = name
    
    def getRandomShape(self):
        return tetris_shapes[rand(len(tetris_shapes))]
